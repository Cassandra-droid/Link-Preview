import Head from "next/head";
import { ReactNode } from "react";
import { Inter } from "next/font/google";
import type { Metadata } from "next";
import "./globals.css";

interface RootLayoutProps {
  children: ReactNode;
}

const inter = Inter({ subsets: ["latin"] });

// ExtendedMetadata interface to include additional OG properties
interface ExtendedMetadata extends Metadata {
  ogImage?: string;
  ogUrl?: string;
  title?: string;
}

// Define metadata with optional fields including OG properties
export const metadata: ExtendedMetadata = {
  title: "Create Next App",
  description: "Generated by create next app",
  ogImage: "https://link-preview-eta.vercel.app/assets/header.png",
  ogUrl: "https://link-preview-eta.vercel.app/",
};

export default function RootLayout({ children }: RootLayoutProps) {
  return (
    <>
      <Head>
        <html lang="en" />
        <meta property="og:title" content={metadata.title || undefined} />
        <meta
          property="og:description"
          content={metadata.description || undefined}
        />
        <meta property="og:url" content={metadata.ogUrl || undefined} />
        <meta property="og:image" content={metadata.ogImage || undefined} />
        <meta property="og:type" content="website" />
      </Head>
      <body className={inter.className}>{children}</body>
    </>
  );
}
